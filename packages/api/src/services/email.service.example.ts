/**
 * Email Service Usage Examples
 * 
 * This file demonstrates how to use the EmailService in the authentication system.
 * These examples show integration points for the auth service.
 */

import { emailService } from './email.service.js';

/**
 * Example 1: Send verification email after user registration
 * 
 * This should be called in the auth service after creating a new user.
 */
export async function exampleSendVerificationEmail() {
  const user = {
    email: 'newuser@example.com',
    name: 'John Doe',
  };
  
  const verificationToken = 'generated-verification-token-123'; // Generated by auth service
  
  try {
    await emailService.sendVerificationEmail(
      user.email,
      verificationToken,
      user.name
    );
    console.log('Verification email sent successfully');
  } catch (error) {
    // Email service handles errors gracefully - this won't throw
    // But you can log for monitoring purposes
    console.log('Email service completed (check logs for details)');
  }
}

