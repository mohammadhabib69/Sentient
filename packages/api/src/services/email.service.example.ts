/**
 * Example 4: Integration in auth service registration flow
 * 
 * This shows how the email service fits into the complete registration flow.
 */
export async function exampleRegistrationFlow() {
  // 1. Validate input (done by auth service)
  const input = {
    name: 'New User',
    email: 'newuser@example.com',
    password: 'SecurePass123!',
  };
  
  // 2. Create user in database (done by auth service)
  const user = {
    id: 'user-uuid',
    name: input.name,
    email: input.email,
    emailVerified: false,
  };
  
  // 3. Generate verification token (done by auth service)
  const verificationToken = 'random-token-generated-by-crypto';
  const hashedToken = 'bcrypt-hashed-token'; // Stored in database
  
  // 4. Send verification email (using email service)
  await emailService.sendVerificationEmail(
    user.email,
    verificationToken, // Send unhashed token in email
    user.name
  );
  
  // 5. Return response to client
  console.log('User registered successfully, verification email sent');
}

/**
 * Example 5: Development mode behavior
 * 
 * When RESEND_API_KEY is not set or NODE_ENV is 'development' or 'test',
 * emails are logged to console instead of being sent via Resend.
 */
export async function exampleDevelopmentMode() {
  // In development, this will log to console:
  // === EMAIL (Development Mode) ===
  // From: noreply@sentient.dev
  // To: dev@example.com
  // Subject: Verify your email address
  // ... (full email content)
  
  await emailService.sendVerificationEmail(
    'dev@example.com',
    'dev-token',
    'Developer'
  );
}

/**
 * Example 6: Error handling
 * 
 * The email service handles errors gracefully without throwing.
 * This ensures that email failures don't block user registration.
 */
export async function exampleErrorHandling() {
  // Even if Resend API fails, this won't throw an error
  // The error will be logged to console.error
  // User registration can proceed successfully
  
  await emailService.sendVerificationEmail(
    'user@example.com',
    'token',
    'User'
  );
  
  // This code will always execute, even if email sending fails
  console.log('Registration completed successfully');
}
